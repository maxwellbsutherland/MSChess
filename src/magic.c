Magic bishop_magics[64];
Magic rook_magics[64];

void initialize_bishop_magics(void) {
    for (U8 sq = a1; sq <= h8; sq++) {
        const U64 mask = bishop_masks[sq];

        Magic magic = {
            .ptr = bishop_magic_masks[sq],
            .mask = mask,
            .magic = bishop_magic_values[sq],
            .shift = 64 - __builtin_popcountll(mask)
        };

        bishop_magics[sq] = magic;
    }
}

void initialize_rook_magics(void) {
    for (U8 sq = a1; sq <= h8; sq++) {
        const U64 mask = rook_masks[sq];

        Magic magic = {
            .ptr = rook_magic_masks[sq],
            .mask = mask,
            .magic = rook_magic_values[sq],
            .shift = 64 - __builtin_popcountll(mask)
        };

        rook_magics[sq] = magic;
    }
}